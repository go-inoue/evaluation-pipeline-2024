import yaml


all_subtasks = [
    "island_effects",
    "binding",
    "argument_structure",
    "ellipsis",
    "verbal_agreement",
    "filler-gap",
    "morphology",
    "nominal_structure",
    "quantifiers",
    "NPI_licensing",
    "control_raising"
]


def main() -> None:
    for task in all_subtasks:
        file_name = f"{task}.yaml"
        try:
            with open(f"{file_name}", "w", encoding="utf-8") as f:
                f.write("# Generated by utils.py\n")
                yaml.dump(
                    {
                        "include": "_template_yaml",
                        "task": "jblimp_bunsetsu_" + task,
                        "dataset_kwargs": {"data_files": f"evaluation_data/jblimp_bunsetsu/{task}.jsonl"}
                    },
                    f,
                )
        except FileExistsError:
            pass


if __name__ == "__main__":
    main()